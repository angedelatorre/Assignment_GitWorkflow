package com.sample1.foodorder;

public class Order {

    double burger = 0;
    double pasta = 0;
    double cola = 0;
    double coffee = 0;

    public double getBurger() {
        return burger;
    }

    public void setBurger(double burger) {
        this.burger = burger;
    }

    public double getPasta() {
        return pasta;
    }

    public void setPasta(double pasta) {
        this.pasta = pasta;
    }

    public double getCola() {
        return cola;
    }

    public void setCola(double cola) {
        this.cola = cola;
    }

    public double getCoffee() {
        return coffee;
    }

    public void setCoffee(double coffee) {
        this.coffee = coffee;
    }

    public Order() {
   }
}
